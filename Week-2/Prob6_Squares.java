/* Problem 6:
Print the squares of numbers from 1 to 15 using a for loop */
public class Prob6_Squares{
    public static void main(String[] args){
        System.out.println("Squares of numbers from 1 to 15:");
        for (int i = 1; i <= 15; i++){
            System.out.println(i * i);
        }
    }
}
